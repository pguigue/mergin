<template>
  <v-menu
    v-model="showMenu"
    :position-x="x"
    :position-y="y"
    absolute
    offset-y
    :min-width="150"
  >
    <v-list>
      <v-list-item @click="deleteFile">
        <v-list-item-title>Delete</v-list-item-title>
      </v-list-item>
    </v-list>
  </v-menu>
</template>

<script lang="ts">
import Vue from 'vue'
import { mapMutations } from 'vuex'

export default Vue.extend({
  data() {
    return {
      showMenu: false,
      x: 0,
      y: 0,
      file: null
    }
  },
  methods: {
    ...mapMutations('projectModule', ['deleteFiles']),
    open(evt, file) {
      this.showMenu = true
      this.x = evt.clientX
      this.y = evt.clientY
      this.file = file
    },
    deleteFile() {
      this.deleteFiles({ files: [this.file.path] })
    }
  }
})
</script>
